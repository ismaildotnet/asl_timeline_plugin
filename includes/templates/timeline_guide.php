<div class="usage-guide">
    <h3>Setting Up</h3>
    <ol class="list-unstyled" type="1">
        <li class="list-item">Click <strong>Timeline</strong> from the menu</li>
        <li class="list-item">Navigate to the timeline settings and click on it</li>
        <li class="list-item">Configure <b>Color</b> based on your choice and hit <strong>save settings</strong></li>
        <li class="list-item">Go to <u>switch</u> tab to enable or disable the feature and hit <strong>save settings</strong></li>
        <li class="list-item">Go to <b>Number</b> tab to set different numerical parameters and hit <strong>save settings</strong></li>
    </ol>

    <h3>Adding Timeline Item</h3>
    <ol class="list-unstyled" type="1">
        <li class="list-item">Click Timeline from the menu and then click add new timeline item</li>
        <li class="list-item">Insert your desired value such as date, feature image, content, icon, etc.</li>
        <li class="list-item">Hit <strong>save settings</strong></li>
        <li class="list-item">Repeat this step to add more timeline items</li>
    </ol>

    <h3>Displaying Timeline</h3>
    <ol class="list-unstyled" type="1">
        <li class="list-item">Edit Page or Create a New one from the menu</li>
        <li class="list-item"><code>[timeline_shortcode]</code> Copy this shortcode</li>
        <li class="list-item">Add an HTML block or edit as HTML, paste the copied code</li>
        <li class="list-item">Hit <strong>Save/Update/Publish</strong> based on availability</li>
    </ol>

    <hr>

    <h3>Displaying as Gutenberg Timeline</h3>
    <ol class="list-unstyled" type="1">
        <li class="list-item">Go to Pages/Posts</li>
        <li class="list-item">Add New or Edit an existing one where you want to show the timeline</li>
        <li class="list-item">Click <code>+</code> icon to add a block</li>
        <li class="list-item">Search Timeline block & hit click</li>
        <li class="list-item">Again click on Add Timeline Item</li>
        <li class="list-item">Configure Title, Date, Thumbnail, and Description</li>
        <li class="list-item">Publish the Page/Post</li>
    </ol>
</div>
