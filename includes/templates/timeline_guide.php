<h3>Settings Up</h3>
<ol class="list-unstyled" type="1">
    <li class="list-item">Click <strong>Timeline</strong> from menu </li>
    <li class="list-item">You will see the timeline settings and lick on it</li>
    <li class="list-item">Configure <b>Color</b> based on your choice and hit <strong>save settings</strong></li>
    <li class="list-item">go to <u>switch</u> tab to enable or disble feature and hit  <strong>save settings</strong></li>
    <li class="list-item">go <b>Number</b> tab to settings different numarical parameter and hit <strong>save settings</strong></li>
</ol>
